#include<iostream>

using namespace std;

int main (){
            // This is the first for loop:
int j = 1 , k = 6;
for (int j = 1; j<=10; j++ )
{
    cout<<j*k<<endl;
}

          // This is the 2nd while loop:
int j = 1, k =6;
while (j<=10)
{
    cout<<j*k<<endl;
j++;
}

           // This is 3rd do-while loop:
int j = 1 , k =6;
do 
{
cout<<j*k<<endl;
j++;
} while (j<=10);
    return 0;
}
 
